/**
 * src/services/githubSyncService.ts
 *
 * Persists user progress in a single `progress.json` file inside a public
 * `{user}/solarhub-data` repository using GitHub Contents API.
 *
 * The document stores one cursor per task type so the frontend can resume
 * from the next available item in each JSONL file independently.
 */

import type { TaskType } from './annotationService';
import { getStoredToken, getStoredUser } from './githubAuthService';

const PROGRESS_FILE_NAME = 'progress.json';
const PROGRESS_REPO_NAME = 'solarhub-data';

export interface ProgressDocument {
  schemaVersion: 2;
  points: number;
  streak: number;
  lastActiveDate: string | null;
  lastCompletedIdsByType: Record<TaskType, string | null>;
  updatedAt: string;
}

interface RepoConfig {
  owner: string;
  repo: string;
}

const TASK_TYPES: TaskType[] = [
  'sunspot',
  'magnetogram',
];

function createEmptyLastCompletedIdsByType(): Record<TaskType, string | null> {
  return TASK_TYPES.reduce((acc, taskType) => {
    acc[taskType] = null;
    return acc;
  }, {} as Record<TaskType, string | null>);
}

function getCurrentRepoConfig(): RepoConfig {
  const user = getStoredUser();
  if (!user?.login) {
    throw new Error('User not authenticated');
  }
  return { owner: user.login, repo: PROGRESS_REPO_NAME };
}

let cachedProgress: ProgressDocument | null = null;
let cachedSha: string | undefined;
let writeQueue: Promise<void> = Promise.resolve();

function defaultProgress(): ProgressDocument {
  return {
    schemaVersion: 2,
    points: 0,
    streak: 0,
    lastActiveDate: null,
    lastCompletedIdsByType: createEmptyLastCompletedIdsByType(),
    updatedAt: new Date().toISOString(),
  };
}

function encodeBase64Text(text: string): string {
  return btoa(unescape(encodeURIComponent(text)));
}

